/*
======================================================================
  [핵심 개념] 산술 연산자 (Arithmetic Operators)
======================================================================
  우리가 수학 시간에서 배웠던 덧셈, 뺄셈, 곱셈, 나눗셈을 컴퓨터에서 
  수행하기 위해 사용하는 기호들입니다.

  [산술 연산자 5가지]
  +   (더하기)  : 두 값을 더합니다.      (예: 6 + 2 = 8)
  -   (빼기)    : 앞의 값에서 뒤를 뺍니다. (예: 5 - 3 = 2)
  * (곱하기)  : 두 값을 곱합니다.      (예: 2 * 3 = 6)
  /   (나누기)  : 몫을 구합니다.         (예: 8 / 2 = 4)
  %   (나머지)  : 나눈 후 남은 값을 구합니다. (예: 8 % 3 = 2)
  
  ※ '%' 기호는 짝수/홀수 판별이나 배수 판별에 아주 많이 쓰입니다!
*/
public class Opr01 {

	public static void main(String[] args) {
		
		int a = 10, b = 4,  c;
		
		c = a + b;
	//  0 = 10 + 4;
	//  14	
		System.out.println(a  + " + " + b + " = " + c); //"10 + 4 = 14"
						 //10 + " + " + 4 + " = " + 14
						//"10"+ " + "
						//	 "10 + " + 4
						//   "10 + " + "4"
						//    	  "10 + 4"+ " = "
						//            "10 + 4 = " + 14
						//			  "10 + 4 = " + "14"
						//             	   "10 + 4 = 14"

//현재 
//		int a = 10, b = 4,  c = 14;
		
		c  =  a  - b;
//		14 =  10 - 4;
//		   =     6;
//		6  =
		
		System.out.println(a + " - " + b + " = " + c);// "10 - 4 = 6" 
		
//현재 
//		int a = 10, b = 4,  c = 6;
		
		c = a  *  b;
//		6 = 10 *  4;
//		  =    40;
//	    40=  
		
		System.out.println(a + " X " + b + " = " + c);//"10 X 4 = 40"

//현재
//		int a = 10,  b = 4,  c = 40;
		
		c = a / b;
		//10 나누기 4의 몫은 2.5가 아니라 2입니다. (정수끼리의 나눗셈이므로 소수점 버림)
		
		System.out.println(a + " / " + b + " = " + c); //"10 / 4 = 2"
		
//현재
//		int a = 10,  b = 4,  c = 2;
		
		c = a % b;
		//10을 4로 나눴을때 몫은 2이고, '나머지'가 2가 남습니다.
		//-> 10을 4나눴을때 나머지값 2구하기 
		
		System.out.println(a + " % " + b + " = " + c); //"10 % 4 = 2"
		
		
		
		
	}

}












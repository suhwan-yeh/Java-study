public class PhoneTest {
	public static void main(String[] args) {

		SmartPhone phone = new SmartPhone();
		// 이 객체 "1개" 안에 battery가 2개 존재하게 됩니다:
		//   부모 영역 battery = 100 (보조배터리)
		//   자식 영역 battery = 50  (휴대폰 배터리)

		System.out.println("===== 처음 상태 =====");
		phone.showBattery();
		// -> 100% / 50% 가 출력되어야 합니다

		System.out.println();
		System.out.println("===== 앱 사용 =====");
		phone.useApp();
		// -> 휴대폰 배터리만 50 - 20 = 30으로 줄고,
		//    보조배터리는 100 그대로여야 합니다

		System.out.println();
		System.out.println("===== 보조배터리로 충전 =====");
		phone.charge();
		// -> 보조배터리 100 - 30 = 70,
		//    휴대폰 배터리 30 + 30 = 60이 되어야 합니다
	}
}


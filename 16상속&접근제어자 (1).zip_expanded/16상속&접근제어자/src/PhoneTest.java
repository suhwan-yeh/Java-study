// ★ 응용문제. 휴대폰과 보조배터리 - this 와 super.변수 사용하기 ★
//--------------------------------------------------------------------------
// [이 문제로 연습하는 것 - 딱 3가지만!]
//   this.변수     : "내(자식) 객체 영역"의 변수에 접근
//   this.메소드() : "내 객체"의 다른 메소드 호출
//   super.변수    : "부모 영역"의 변수에 접근
//
//
// [만들 프로그램 소개]
//   기본폰(BasicPhone)을 부모, 스마트폰(SmartPhone)을 자식으로 만듭니다.
//   부모와 자식이 "같은 이름"의 battery 변수를 각각 가지는데,
//   - 부모 영역의 battery = 보조배터리 잔량 (100%로 시작)
//   - 자식 영역의 battery = 휴대폰 자체 배터리 잔량 (50%로 시작)
//   앱을 쓰면 휴대폰 배터리가 닳고,
//   충전을 하면 보조배터리에서 휴대폰으로 30%가 옮겨갑니다.
//
// 주석의 지시를 위에서부터 순서대로 코드로 옮기면 완성됩니다.
// 파일 맨 아래 [예상 실행 결과]와 출력이 같으면 정답입니다.
//==========================================================================


//==========================================================================
// [부모 클래스] BasicPhone
//==========================================================================
class BasicPhone {

	//[1] 객체 변수 1개 선언 + 명시적 초기화
	//    protected 접근제어자, int 자료형, 변수명 battery, 초기값 100
	//    작성 형태:  protected int battery = 100;
	//    - 이 변수는 "보조배터리 잔량"으로 사용합니다.
	//    - protected이므로 자식 클래스에서 super.battery 로 접근 가능합니다.
	//    ※ 부모 클래스에는 이 변수 1개 말고 아무것도 만들지 않습니다!
	//      (생성자도, 메소드도 없음 -> 기본생성자는 자동으로 생김)

	//>>> 여기에 변수를 선언+초기화하세요.
	protected int battery = 100;
	
	/*
	부모의 기본생성자 여기 있는 것이다.
	
	public BasicPhone() {
		
	}
	*/	
}



//==========================================================================
// [자식 클래스] SmartPhone
//==========================================================================
//[2] SmartPhone 클래스 전체 작성
//    class SmartPhone extends BasicPhone {  으로 시작하세요.
//    ※ 생성자는 만들지 않습니다!
//
//    SmartPhone 내부에 작성할 것 4가지:
//
//    (1) 객체 변수 1개 선언 + 명시적 초기화
//        protected int battery = 50;
//        - ★부모와 "같은 이름"으로 일부러 선언합니다!★
//          이러면 객체 메모리 안에 부모 영역의 battery[100]과
//          자식 영역의 battery[50]이 "둘 다" 존재하게 됩니다.
//        - 이 변수는 "휴대폰 자체 배터리 잔량"으로 사용합니다.
//        - 이제부터 두 배터리를 구분하는 방법:
//            super.battery -> 부모 영역 (보조배터리)
//            this.battery  -> 자식 영역 (휴대폰 배터리)
//
//    (2) showBattery 메소드 (두 배터리 잔량을 나란히 출력)
//        문법:  public void showBattery() { 출력코드 2줄 }
//        - 1줄째:
//          System.out.println("보조배터리 잔량(부모) : " + super.battery + "%");
//          * super.battery : 부모 영역의 battery를 콕 집어 읽습니다.
//        - 2줄째:
//          System.out.println("휴대폰 배터리 잔량(자식): " + this.battery + "%");
//          * this.battery : 자식 영역의 battery를 읽습니다.
//
//    (3) useApp 메소드 (앱 사용 -> 휴대폰 배터리 20% 소모)
//        문법:  public void useApp() { 코드 3줄 }
//        - 1줄째:
//          this.battery = this.battery - 20;
//          * 자식 영역의 battery에서 20을 빼서 다시 저장합니다.
//          * 보조배터리(super.battery)는 건드리지 않습니다!
//        - 2줄째:
//          System.out.println(">> 앱을 사용해서 휴대폰 배터리가 20% 소모되었습니다");
//        - 3줄째:
//          this.showBattery();
//          * ★this.메소드() 연습★ 내 객체의 showBattery()를 호출해서
//            변화된 잔량을 바로 확인합니다.
//            (같은 클래스 안의 메소드는 this.를 붙여 호출할 수 있습니다)
//
//    (4) charge 메소드 (보조배터리 30%를 휴대폰으로 옮겨 충전)
//        문법:  public void charge() { 코드 4줄 }
//        - 1줄째:
//          super.battery = super.battery - 30;
//          * ★super.변수에 "값을 저장"하는 연습★
//            부모 영역의 보조배터리에서 30을 빼서 다시 저장합니다.
//        - 2줄째:
//          this.battery = this.battery + 30;
//          * 자식 영역의 휴대폰 배터리에 30을 더합니다.
//          * 1줄째와 2줄째가 한 세트: 배터리가 "이동"하는 것입니다.
//        - 3줄째:
//          System.out.println(">> 보조배터리 30%를 휴대폰으로 옮겨 충전했습니다");
//        - 4줄째:
//          this.showBattery();

//>>> 여기에 SmartPhone 클래스 전체를 작성하세요.
class SmartPhone extends BasicPhone{
	
	 //(1) 객체 변수 선언 + 명시적 초기화 - 부모와 "같은 이름"으로!
	 protected int battery = 50;
	 
		// ★핵심★ 부모 BasicPhone에도 battery가 있는데 자식이 같은 이름으로 또 선언했다.
		// 그 결과 SmartPhone 객체 "1개" 안에는
		//   - 부모 영역의 battery [ 100 ]  <- 보조배터리
		//   - 자식 영역의 battery [ 50  ]  <- 휴대폰 배터리
		// 가 "둘 다" 존재한다. 하나가 다른 하나를 덮어쓰는 것이 아니다!
		// 이제 두 배터리를 구분하는 유일한 방법이 super. 와 this. 이다:
		//   super.battery -> 부모 메모리영역 의 객체 변수 (보조배터리 100)
		//   this.battery  -> 자식 메모리영역 의 객체 변수 (휴대폰 배터리 50)	
	 
	 //(2) showBattery 메소드 (두 배터리 잔량을 나란히 출력)
	 public void showBattery() {
		 
		 System.out.println("보조베터리 잔량(부모) : " + super.battery + "%");
		 // super.battery : "부모 영역"의 battery를 콕 집어 읽는다.
		 // 처음 상태라면 100이 출력된다.
		 
		 System.out.println("휴대폰 베티리 잔량(자식) : " + this.battery + "%");
		 // this.battery : "자식 영역"의 battery를 읽는다.
		 // 처음 상태라면 50이 출력된다.
		 // 같은 battery라는 이름인데 super냐 this냐에 따라
		 // 완전히 다른 메모리 공간을 읽는 것이 이 문제의 핵심!
	 
	 }
	 // (3) useApp 메소드 (앱 사용 -> 휴대폰 배터리 20% 소모)
	 public void useApp() {
		 
		 this.battery  = this.battery - 20;
		// 자식 영역의 battery에서 20을 빼서 다시 저장.
		// 예) 처음 실행이면: this.battery = 50 - 20 = 30
		// ★보조배터리(super.battery)는 이 줄과 무관하므로 100 그대로!★
		// -> this와 super가 서로 다른 메모리라는 증거가 출력에서 확인된다.
		 
		 System.out.println(">> 앱을 사용해서 휴대폰 배터리가 20% 소모되었습니다");
		 //무슨일이 일어 났는지  안내 구문 출력
		 
		 this.showBattery();
		// ★this.메소드() 연습★
		// 내 객체의 showBattery()를 호출해서 변화된 잔량을 바로 확인.
		// 같은 클래스 안의 메소드는 이렇게 this.를 붙여 호출할 수 있다.
		// (this.를 생략하고 showBattery(); 라고만 써도 동작은 같지만,
		//  "내 객체의 메소드를 부른다"는 것을 분명히 하려고 붙인 것)
		 		 
	 }
	 
	 // (4) charge 메소드 (보조배터리 30%를 휴대폰으로 옮겨 충전)
	 public void charge() {
		 
		 super.battery = super.battery - 30;
		// ★super.변수에 "값을 저장"하는 연습★
		// 부모 영역의 보조배터리에서 30을 빼서 다시 저장.
		// 예) super.battery = 100 - 30 = 70
		// super.battery는 읽기(오른쪽)뿐 아니라
		// 저장(왼쪽)에도 쓸 수 있다는 것을 보여주는 줄.
		 
		 this.battery = this.battery + 30;
		// 자식 영역의 휴대폰 배터리에 30을 더한다.
		// 예) this.battery = 30 + 30 = 60
		// 윗줄과 이 줄이 한 세트: 보조배터리에서 뺀 30이
		// 휴대폰으로 "이동"한 것이다. (두 잔량의 합 130은 그대로 유지!)
		 
		 System.out.println(">> 보조배터리 30%를 휴대폰으로 옮겨 충전했습니다");
		 
		 this.showBattery(); //충전 결과를 바로 확인 
	 }
	 
}



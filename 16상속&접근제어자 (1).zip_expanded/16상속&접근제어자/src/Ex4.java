// 주제 : 부모 클래스와 자식 클래스가 "같은 패키지"에 만들어져 있을 경우
//        접근제어자들을 사용한 멤버(변수 또는 메소드)의 접근 범위 알아보기
//        ★비전공자용 상세 주석판★
//==========================================================================
//
// [먼저 큰 그림부터 - 접근제어자 4종을 "문 4종류"로 이해하기]
//
//   접근제어자란? 변수나 메소드 앞에 붙여서
//   "누가 이 변수/메소드에 접근해도 되는지"를 정하는 자물쇠 설정이다.
//
//   잠금이 느슨한 순서(개방적) -> 엄격한 순서(폐쇄적)로 나열하면:
//
//   ------------------------------------------------------------------
//   접근제어자   | 접근을 허용하는 범위
//   ------------------------------------------------------------------
//   public      | 어디서든 전부 허용 (다른 패키지의 클래스까지 전부)
//   protected   | 같은 패키지 전부 + "다른 패키지의 자식 클래스"까지 허용
//   (default)   | 같은 패키지 안에서만 허용  ※아무것도 안 쓰면 이것!
//   private     | 오직 "그 클래스의 { } 안"에서만 허용
//   ------------------------------------------------------------------
//
//   ★이 예제의 조건: AA(부모)와 BB(자식)가 "같은 패키지"에 있다★
//   -> 그래서 default, protected, public 은 전부 접근 가능하고,
//      오직 private 하나만 막히는 것을 눈으로 확인하는 실습이다.
//==========================================================================

class AA  {  //기존에 만들어져 있는 부모 역할의 클래스 
	
	//---------------------------------------------------------
	// [1] default(디폴트) 접근제어자
	//  - "아무 접근제어자도 안 쓰면" 자동으로 default가 된다.
	//    (default라는 단어를 직접 쓰는 게 아니라, 안 쓰는 것이 default!)
	//  - 허용 범위 : 같은 패키지 안이라면 자식 클래스든 일반 클래스든
	//                모두 접근 가능. 패키지가 다르면 무조건 불가.
	//---------------------------------------------------------		
			   int  i;  //자료형 int 앞에 아무것도 작성하지 않으면? defaiult접근제어자를 작성 한것입니다.
			   
	//---------------------------------------------------------
	// [2] protected(프로텍티드) 접근제어자
	//  - 허용 범위 1 : 같은 패키지에 만들어져 있는 다른 클래스에서 접근 가능
	//                  (이 부분은 default와 똑같다)
	//  - 허용 범위 2 : 다른 패키지의 "일반" 클래스에서는 접근 불가능하지만,
	//                  단! 다른 패키지에 만들어 놓은 AA의 "자식클래스"
	//                  내부에서만은 예외적으로 접근 가능
	//  - 즉, default보다 딱 한 가지 더 열려있다:
	//    "패키지가 달라도 내 자식이라면 허용" 이 한 가지!
	//---------------------------------------------------------		
	protected  int  pro;		
			
	//---------------------------------------------------------
	// [3] public(퍼블릭) 접근제어자
	//  - 허용 범위 : 다른 패키지든 같은 패키지든, 모든 클래스에서 접근 허용!
	//  - 비유 : "잠금 없는 문 - 누구나 출입 가능"
	//---------------------------------------------------------
	public    int   pub;
	
	//---------------------------------------------------------
	// [4] private(프라이빗) 접근제어자
	//  - 허용 범위 : 오직 이 AA 클래스의 { } 안에서만 접근 가능.
	//    ★같은 패키지여도, 심지어 자식 클래스(BB)여도 접근 불가!★
	//  - 실무 습관 : 인스턴스 변수를 선언할 때 대부분의 개발자들은
	//    이 private을 가장 많이 사용한다.
	//    (변수를 함부로 못 건드리게 잠가두고, 아래처럼 public 메소드를
	//     통해서만 값을 넣고 꺼내게 하는 것이 안전하기 때문 = 캡슐화)
	//---------------------------------------------------------
	private  int   pri;
	
	//---------------------------------------------------------
	// getter : private 변수 pri에 저장된 값을 "외부로 반환"해주는 공식 통로
	//  - pri 자체는 잠겨있지만, 이 메소드가 public이므로
	//    바깥에서는 getPri()를 호출해서 값을 "얻어올 수는" 있다.
	//---------------------------------------------------------
	public int getPri() {
		return this.pri;  // return this.pri : "이 객체가 소유한" pri 객체변수의 현재값을 돌려준다.
	}

	//---------------------------------------------------------
	// setter : private 변수 pri에 저장된 값을 "변경"해주는 공식 통로
	//  - 바깥에서 pri = 3; 은 불가능하지만 setPri(3); 은 가능하다.
	//---------------------------------------------------------
	public void setPri(int pri) {
		this.pri = pri;
	}

}

//==========================================================================
//부모 AA클래스를 상속받아 새롭게 설계하는 자식 역할의 BB클래스
//★AA와 BB는 지금 같은 파일 = 같은 패키지에 있다는 점을 기억!★
//==========================================================================
class BB extends AA { 
	/*
	 AA 부모클래스로 부터 상속받은  멤버들 (눈에는 안보인다)
	 
			      int i; 
		protected int pro;
		public    int pub;
		private   int pri;
	
		public int  getPri() { return this.pri; }
		public void setPri(int pri) {  this.pri = pri; }
	*/
	
	//BB 자식클래스에 새롭게 추가되는 기능(메소드들)
	
	//---------------------------------------------------------
	// default 접근제어자를 이용한 set 객체 메소드
	// (void 앞에 아무것도 안 붙었으므로 이 메소드 자체도 default)
	//
	// 이 메소드 안에서 부모의 변수 4종(i, pro, pub, pri)에
	// 하나씩 접근을 시도하며 "무엇이 되고 무엇이 안 되는지" 확인한다.
	//---------------------------------------------------------
	void  set() { 
		
		super.i = 1; //[성공] i변수는 default 접근제어자로 만든 변수.
					 // BB는 AA와 "같은 패키지"에 있으므로 접근 가능!
		
		super.pro = 2; //[성공]  pro변수는 protected 접근제어자로 만든 변수.
					   //protected는 같은 패키지면 무조건 허용이므로 접근가능!
					   //게다가!  BB는 자식이기까지 하니 두 가지 자격으로 통과)
		
	  //super.pri = 3; //[실패] pri변수는 private 접근제어자로 만든 변수.
					   //같은 패키지 여도, 자식클래스여도 접근 불가능!
					   //priavte은 "AA 클래스의  {   } 안"에서만 접근 가능하기 떄문.
		
		super.setPri(3);
		// [성공 - 우회로] pri를 직접은 못 바꾸지만,
		//        부모가 public으로 열어둔 setPri 메소드를 호출하면
		//        그 메소드가 (AA 클래스 내부이므로) 대신 pri에 3을 저장해준다.
		//        -> private 변수는 항상 이렇게 "공식 통로"로만 다룬다!
		
		super.pub = 4; //[성공]  pub변수는 public 접근제어자로 만든 변수.   어디서든지 접근 가능하므로 당연히 성공.
		
	} // set() <----  class BB 자식클래스의 메소드 
	
	//---------------------------------------------------------
	// public 접근제어자를 이용한 get 객체 메소드
	//  - 부모 영역의 변수 4개 값을 전부 모아 한 줄의 문자열로 반환한다.
	//  - 여기서도 pri만큼은 직접 못 읽으므로 getPri()를 통해 얻는다.
	//---------------------------------------------------------
	public String get() {
		
		return super.i + "," + super.pro + "," + super.pub +  "," + super.getPri();
		
		// 예) i=1, pro=2, pub=4, pri=3 이라면 -> "1,2,4,3" 문자열을 만들어 반환
		// (pri 값 3이 맨 마지막에 오는 이유: 코드에서 getPri()를 맨 뒤에 썼기 때문)
	}
	

}//------------------------------------------>   class BB{}

public class Ex4 {

	public static void main(String[] args) {
		
		//BB 자식 클래스의 객체 메모리 생성
		//-> 이 BB자식객체 메모리 "한 개" 안에  AA부모객체 메모리 영역 +  BB자식객체 메모리 영역이 함꼐 만들어진다.
		BB  bb  = new BB();
		
		//set() 호출  -> 위에서 본것  처럼 부모메모리 영역의 변수들에 값이 저장됨 
			bb.set();
		
		//get() 호출  -> 저장된 값들을 문자열로 받아 출력
		System.out.println(  bb.get()   );  //"1,2,4,3"

		/*
		  0x12   전체 new BB(); 객체 메모리 영역
		================================================

		---------- AA부모 메모리 영역 ----------
		           int i;    [ 1 ]   <- default  : 같은 패키지라 접근 성공
		 protected int pro;  [ 2 ]   <- protected: 같은 패키지라 접근 성공
		 public    int pub;  [ 4 ]   <- public   : 어디서든 접근 성공
		 private   int pri;  [ 3 ]   <- private  : 직접 접근은 실패!
		                                            setPri(3)를 통해서만 저장됨
		 //getter
		 public int getPri(){ }
		 //setter
		 public void setPri(int pri){ }


		---------- BB자식 메모리 영역 ----------
		        void set(){  super.i=1;  super.pro=2;  super.setPri(3);  super.pub=4;  }
		 public String get(){ }
		------------------------------------------------


		================================================

		※ private인 pri도 메모리에는 [ 3 ] 이라고 값이 잘 들어있다!
		   "존재하지 않는 것"이 아니라 "직접 접근하는 코드만 금지"인 것.
		*/
	
	}

}


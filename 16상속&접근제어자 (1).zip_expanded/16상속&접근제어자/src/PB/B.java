

package PB; //현재 B클래스가 PB패키지 경로에 만들어져 있다고!
			//자바 컴파일러에게 알려주는 패키지 선언문!

/*
 다른 패키지 PA에 만들어져 있는 A클래스를 현재 PB패키지의 B클래스 내부에서 사용하기위해
 import 구문을 작성해서 자바 컴파일러에게 알려주는 구문!
 
 작성 방법
 import 외부다른패키지명.사용할외부클래스명;
*/
import PA.A;

//★이 예제의 상황 정리★
//- 부모 A클래스는 PA패키지에, 자식 B클래스는 PB패키지에 있다.
//즉 "서로 다른 패키지" + "상속 관계" 라는 조건이다.
//- 이 조건에서 부모의 변수 4개(i, pro, pri, pub)에 접근한 결과:
//  default(i)     -> 접근 불가 (패키지가 다르므로, 자식이어도 안 됨!)
//  protected(pro) -> 접근 가능 (패키지가 달라도 "자식 클래스"라서 예외 허용)
//  private(pri)   -> 접근 불가 (어디서든 A클래스 밖에서는 불가)
//  public(pub)    -> 접근 가능 (어디서든 가능)
//- ★같은 패키지였다면 default도 가능했다. 패키지가 달라지면서
//default가 막히고, protected만 "자식 자격"으로 살아남는 것이
//이 예제의 핵심 비교 포인트!★


//다른 패키지 PA에 만들어져 있는 부모 A클래스의 멤버를 상속받아 새로운 자식 B클래스 만들기 
public class B extends A {

	void set() {		
		//[1] default 변수 i
		//    부모 A클래스에서 접근제어자를 아무것도 안 붙여 만든 변수
		//	    default는 "같은 패키지"에서만 접근을 허용하는데,
		//    지금 B는 다른 패키지(PB)에 있으므로 자식 클래스여도 접근 불가!
		//super.i = 1;
	
		//[2] protected 변수 pro
		//	  protected는 "같은 패키지" 또는 "다른 패키지의 자식 클래스"에게
		//    접근을 허용한다. B는 다른 패키지지만 A의 자식이므로 접근 가능!
		//    (default와의 차이가 바로 이 "자식 예외" 한 가지다)
		super.pro = 2;
		
		//[3] private 변수 pri
		//	  private은 "선언한 A클래스의 { } 안"에서만 접근 허용.
		//    같은 패키지든 다른 패키지든, 자식이든 아니든 전부 불가!
		//super.pri = 3;
		
		//[4] public 변수 pub
		//	  public은 패키지가 달라도, 상속 관계가 아니어도
		//    어디서든 접근을 허용한다. 당연히 접근 가능!	
		 pub = 4;
//	     pub = 4; 역시 super.을 붙여 부모 영역임을 명시할 수 있다.
// super.pub = 4;
	}
	
	public static void main(String[] args) {
		
		B  b   = new B();
		   b.set();          //pro에 2, pub에 4 저장  (i와 pri는  접근 불가라 건드리지 못함)
		   b.print();        //부모에게 상속받은 print() 메소드 호출
		   
			//★출력 결과: i=0,pro=2,pri=0,pub=4입니다.
			//
			//  i=0,  pri=0 인 이유:
			//  - set()에서 접근이 막혀 값을 저장하지 못했으므로
			//    int의 기본값 0이 그대로 남아있는 것.
			//  - 단, print()는 부모 A클래스 "내부"에 작성된 메소드라서
			//    자기 것인 i와 pri를 this.i / this.pri 로 읽어 출력할 수 있다.
			//  - 즉 "B에서 저장은 못 하지만, A 자신의 메소드는 읽을 수 있다"
			//    -> 접근제어자는 변수의 존재를 없애는 것이 아니라
			//       접근하는 코드의 "위치"를 제한하는 것임을 보여준다.

						/*
					 				new B(); 전체 자식객체 메모리 단 1개 생성
					 	===================================================================
						   ---------------------------------------------------------

								A부모객체 메모리 영역 내부 모습

										int i;   [ 0 ]  //default  : 다른 패키지라 접근 불가 -> 기본값 0
							protected 	int pro; [ 2 ]  //protected: 다른 패키지지만 자식이라 접근 성공
							private		int pri; [ 0 ]  //private  : 어디서든 불가 -> 기본값 0
							public 		int pub; [ 4 ]  //public   : 어디서든 가능 -> 접근 성공

							public A(){}  <---기본생성자

							public void print() {
								System.out.print("i=" + this.i + "," );
								System.out.print("pro=" + this.pro + ",");
								System.out.print("pri=" + this.pri + ",");
								System.out.println("pub=" + this.pub + "입니다.");
							}
						   ---------------------------------------------------------
						        B자식객체 메모리 영역 내부 모습

						   		public B(){}   <---기본생성자
						   		(생성자를 안 만들었으므로 컴파일러가 자동 생성)

						        void set(){
						        	pro = 2;   // super.pro = 2; 와 동일
						        	pub = 4;   // super.pub = 4; 와 동일
						        }

						   ----------------------------------------------------------
				 	===================================================================

						★같은 패키지 예제(Ex4: AA->BB)와의 비교★
						  같은 패키지였을 때 : i(성공), pro(성공), pri(실패), pub(성공)
						  다른 패키지인 지금 : i(실패), pro(성공), pri(실패), pub(성공)
						  -> 패키지가 달라지며 바뀐 것은 default(i) 딱 하나!
						  -> protected가 default보다 넓다는 것이 여기서 증명된다.
					*/		

	}

}

















//주제 : 두 클래스의 상속관계에서 private 접근제어자를 지정한 변수 또는 메소드 사용 실습
//==========================================================================
//
// [이 예제에서 확인할 핵심 3가지]
//
//  1. public  변수 : 어디에서나 자유롭게 읽고 쓸 수 있다.
//
//  2. private 변수 : "그 변수를 선언한 클래스 안"에서만 읽고 쓸 수 있다.
//                    심지어 상속받은 자식 클래스조차 직접 접근할 수 없다.
//
//  3. 그래서 private 변수를 바깥에서 다루고 싶으면,
//     그 클래스가 public으로 열어준 메소드(setN, getN 같은)를
//     "통해서만" 값을 넣고 꺼낼 수 있다.
//     -> 이것이 바로 setter(값 저장 담당)와 getter(값 반환 담당)이다.
//==========================================================================


//======================
//부모 클래스 A
//======================

//참고. Object 클래스는 자바에서 모든 클래스의 부모역할을 하는 클래스 이기떄문에
//     A클래스만 만들어 놓으면 Object클래스를 상속 받고 있는 형태가 된다.

class A  {  // 부모 역할을 하는 클래스 
	
	//변수를 선언하는 문법 :  접근제어자  자료형  변수명;
	
	//---------------------------------------------------------
	// public 변수
	//  -> "공개"라는 뜻 그대로, 어떤 클래스에서든 자유롭게
	//     이 변수에 값을 넣거나(b.p = 5;) 꺼내 쓸 수 있다.
	//---------------------------------------------------------	
	public  int  p;  //p 변수 메모리는 어디서든지 접근 가능 (public 이기 때문)
	
	//---------------------------------------------------------
	// private 변수
	//  -> "비공개"라는 뜻 그대로, 오직 이 A 클래스의 { } 안에서만
	//     직접 접근(n = 10; 이런 식의 코드 작성)이 가능하다.
	//  -> ★중요★ A를 상속받은 자식 클래스(B)조차도
	//     this.n 이나 b.n 처럼 직접 접근하면 컴파일 에러가 난다.
	//  -> 단, "접근을 못 할 뿐"이지 변수 자체는 나중에 객체 메모리 안에
	//     정상적으로 만들어진다. (아래 main의 메모리 그림에서 확인)
	//---------------------------------------------------------
	private int  n;
	//---------------------------------------------------------
	// setter 메소드 : private 변수 n에 "값을 저장"해 주는 공식 통로
	//  -> n에 직접 접근은 막아두고, 이 메소드를 public으로 열어두면
	//     "값을 넣는 것은 허용하되, 반드시 이 문을 통해서만 넣어라"가 된다.
	//  -> 지금은 단순히 저장만 하지만, 나중에는 이 안에
	//     if(n < 0) { ... } 같은 검사 코드를 넣어서
	//     "이상한 값이 들어오는 것을 걸러내는 검문소" 역할까지 하게 된다.
	//     (private + setter/getter 조합을 쓰는 진짜 이유가 바로 이것!)
	//---------------------------------------------------------	
	public void setN(int n) { //매개변수 n : 바깥 클래스에서 전달해 준 새 값이 잠시 담기는 변수 
		
		this.n = n;
		//this.n    :   "이 객체가 소유한" private 변수 n (왼쪽)
		//=         :   매개변수 n의 값을 대입
		//n         :   방금 매개변수로 전달 받은 값        (오른쪽)
		
		// -> 이름이 둘 다 n이라 헷갈리므로, 객체 소유 쪽에 this.를 붙여 구분한다.
		// 예) setN(10) 으로 호출되면 -> this.n = 10;  이 실행되는 것		
	}
	
	//---------------------------------------------------------
	// getter 메소드 : private 변수 n의 "값을 꺼내" 반환해 주는 공식 통로
	//  -> 바깥에서는 a.n 처럼 직접 못 읽는 대신,
	//     a.getN() 을 호출하면 현재 저장된 값을 돌려받을 수 있다.
	public int getN() {
		return this.n;
		// return : 이 메소드를 호출한 쪽으로 값을 "돌려준다"는 뜻
		// 예) n에 10이 저장되어 있다면, getN()을 호출한 자리에 10이 전달됨
	}

}


//========================================================================
//자식 클래스 B  
//-> 기존에 만들어져 있는 A 클래스의 멤버를 상속받아  새로운 기능을 확장한 B자식클래스 만든다.
//=========================================================================
class B extends A {
	// extends A : "B는 A를 상속받는다"는 선언.
	// 이 한 줄 덕분에, 나중에 new B()로 객체를 만들면
	// 그 객체 메모리 안에는 A의 변수(p, n)와 메소드(setN, getN)까지
	// 전부 "포함"된 상태로 만들어진다.
	// (B가 A의 코드를 복사해 붙여넣은 것과 비슷한 효과를 공짜로 얻는 것)	
	/*
	 A클래스로 부터 상속받은 멤버들 눈에 안보이지만 여기 있는 것임 
		public int p;
		private int n;
		public  void setN(int n) {  this.n = n; }
		public  int getN() { return this.n; }
	*/
	//---------------------------------------------------------
	// B 클래스에서만 사용하는 private 변수
	//  -> 이 변수도 private이므로 B 클래스의 { } 안에서만 직접 접근 가능.
	//     main 같은 바깥에서는 b.m = 20; 처럼 쓰면 컴파일 에러가 난다.
	//---------------------------------------------------------
	private int m; // 5
	
	//private 변수 m에 값을 저장하는 setter 메소드 
	public void setM(int m) {
		this.m = m;
		// this.m(B객체 소유의 객체변수)에 매개변수 m의 값을 저장
	}
	
	//private 변수 m의 값을 외부클래스로 반환하는 getter 메소드 
	public int  getM() {
		return  m;
		// this.m 이라고 써도 되고, 겹치는 이름의 매개변수가 없을 때는
		// 이렇게 this를 생략하고 m 이라고만 써도 같은 객체변수 m를 가리킨다.
	}
	
	//---------------------------------------------------------
	// B 객체가 가진 모든 변수 정보를 한 줄의 문자열로 만들어 반환하는 메소드
	//
	//  ★여기가 이 예제의 핵심 포인트★
	//  - m은 "내(B) 것"이므로 this.m 으로 직접 읽을 수 있다.
	//  - 그런데 n은 "부모(A) 것"이면서 private이므로
	//    this.n 이라고 쓰면 컴파일 에러가 난다!
	//  - 그래서 부모가 public으로 열어준 getN() 메소드를 통해
	//    "간접적으로" 값을 얻어와야 한다.
	//
	//  super.getN() 에서 super란?
	//  - super = "부모 쪽"을 가리키는 예약어 (this가 "나 자신"이라면 super는 "부모")
	//  - 사실 이 경우는 그냥 getN() 이라고만 써도 동작하지만,
	//    "이 메소드는 부모에게서 상속받은 것"임을 코드에서 분명히
	//    보여주기 위해 super. 를 붙여 쓴 것이다.
	//---------------------------------------------------------
	public String toString() {
		return  "n=" + super.getN() + ", m=" + this.m;
// 예)          "n=10, m=20" 이라는 문자열을 만들어 반환
	}
	
	// super 예약어란?
	//  - 자식 객체 메모리를 생성하면, 그 안에 부모 메모리 영역이
	//    함께 포함되는데(아래 main의 메모리 그림 참고),
	//    super는 바로 그 "내 안에 포함된 부모 영역"을 가리키는 예약어다.
	
}// class B

public class Ex3 {
	public static void main(String[] args) {
		
		//===========================================================
		// 1. 자식 클래스 객체 생성
		//===========================================================
		// B 객체를 하나 생성.
		// ★중요★ new B() 라고 자식만 만든 것 같지만,
		// 실제 B객체 메모리 전체 영역에는
		//   -> 부모 A 메모리영역 + 자식 B 메모리영역이 "함께" 들어있는
		//      자식 B객체 메모리 1개가 만들어진다.
		// (2층집 하나를 지었는데 1층은 부모 공간, 2층은 자식 공간인 셈.
		//  집은 어디까지나 "한 채"라는 것이 포인트!)
		B  b  =  new B();   
		
		//부모 A클래스의 public 변수
		//->  public 이므로 main메소드 같은 바깥에서도 자유롭게 접근 가능
		b.p = 5; 
		// b가 가리키는 객체 안의 (부모 영역에 있는) p변수에 5를 저장
		
		//부모 A클래스의 private 변수 
		//-> 자식클래스도, main메소드 내부에서도 직접 접근 불가능
		//b.n = 10;   // X 컴파일 에러 발생!
		
		//그래서 부모 A클래스가 public 으로 열어준 setN() 메소드를 통해서
		//private 변수 n에 값을 저장한다.
		b.setN(10);
		//-> setN 메소드 내부에서 this.n = 10; 코드가 실행되어  private int n 변수에  10이 저장됨 
		
		//자식 B클래스의 private 변수도 마찬가지로 적접 접근 불가
		//b.m = 20;    // X 컴파일 에러 발생!
		
		//public 메소드 setM()을 통해 private 변수 m에 값 저장 하자
		b.setM(20);
		//-> setM 메소드 내부에서  this.m = 20; 코드가 실행되어  private int m변수에 20이 저장됨 
		
		//toString()  메소드 호출
		//-> 부모 영역의 n 값  + 자식 영역의 m의 값을 하나의 문자열로 만들어 반환 받아 출력
		System.out.println(  b.toString() );  // "n=10, m=20"
		
		/*
		[B 객체의 실제 메모리 모습]

		Heap 영역
		┌──────────────────────────────────────────┐
		│ B 객체 (객체는 단 1개!)                      │
		│                                          │
		│  부모 A 영역 (1층)                          │
		│   p = 5                                  │
		│   n = 10 (private, 직접 접근 불가)          │
		│                                          │
		│  자식 B 영역 (2층)                          │
		│   m = 20 (private, 직접 접근 불가)          │
		└──────────────────────────────────────────┘

		※ private 변수도 메모리에는 분명히 존재한다!
		※ 단, "코드에서 직접 접근하는 것"만 못 할 뿐이다.
		   (setN/getN 같은 공식 통로로는 얼마든지 값을 넣고 꺼낼 수 있다)
		*/

		//===========================================================
		// 2. 부모 A클래스 객체 생성
		//===========================================================
		
		// 이번에는 A 객체만 생성.
		// 부모만 만들었으므로 이 객체 안에는 자식 B의 변수(m)는 없다.
		// (1층짜리 집만 지은 것 - 자식이 부모를 포함하는 것이지,
		//  부모가 자식을 포함하는 것은 아니라는 것을 확인!)
		A  a = new A();	
		
		   //public 변수 -> 다른 Ex3 클래스에서도 직접 접근 가능 
		   a.p = 5; 
		   
		   // private 변수 -> 바깥(다른 Ex3클래스)에서 직접 코드로 접근 불가능
	    // a.n = 5; // X 컴파일 에러 발생!
		   
		   // public 메소드 setN() 메소드를 통해서라면  private 변수 n에 값 저장 가능 
		   a.setN(5);
		   
				/*
				[A 객체의 실제 메모리 모습 - 생성 직후]
		
				Heap 영역
				┌──────────────────────────────────────────┐
				│ A 객체                                     │
				│                                          │
				│   public  int p = 5   (int 기본값 0)       │
				│   private int n = 0   (int 기본값 0)       │
				└──────────────────────────────────────────┘
				*/
   /*
	===== 실행 결과 =====
	n=10, m=20

	===== 이 예제의 결론 (비전공자용 3줄 요약) =====
	1. private 변수는 "그 클래스 안"에서만 직접 만질 수 있다.
	   자식 클래스라도 예외가 아니다.
	2. 하지만 private 변수도 객체 메모리 안에는 정상적으로 존재하며,
	   public으로 열어둔 setter/getter 메소드를 통해서는
	   얼마든지 값을 넣고 꺼낼 수 있다.
	3. new B()로 자식 객체를 만들면, 그 "한 개"의 객체 안에
	   부모 영역(p, n)과 자식 영역(m)이 함께 들어있다.
*/		

	}

}






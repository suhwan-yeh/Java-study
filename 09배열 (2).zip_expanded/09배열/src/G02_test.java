public class G02_test {
	public static void main(String[] args) {
		
		// 1. 배열 선언과 동시에 직접 초기화 (값을 바로 저장)
		// 4명의 친구 나이(20, 21, 22, 20)를 저장하는 배열 'ages'를 만듭니다.
		// 값을 나열할 때 어떤 기호로 감싸주어야 하는지 떠올려보세요.
		int[]  ages = { 20, 21, 22, 20 };
//		int  []
		
		
		System.out.println("=== 친구들 나이 목록 ===");
		
		// 2. for문을 이용해 데이터 차례대로 꺼내기
		// 총 4명의 나이가 저장되어 있으므로, 배열의 시작 인덱스와 끝나는 조건을 채워주세요.
		for(int i = 0; i < 4; i++) {
			
			// G02.java 예제처럼 (i+1)을 사용해 "1번 친구", "2번 친구" 형식으로 출력합니다.
			System.out.println( (i+1) + "번 친구 나이 => " + ages[i] );
			
		}

	}
}
/*

빈칸 채우기 문제: 5일간의 온도 기록
		
아래 코드의 설명과 문맥을 잘 읽고, [ 빈칸 1 ] 부터 [ 빈칸 5 ] 까지 들어갈 알맞은 코드를 생각해 보세요.
*/
public class G01_test {
	
	public static void main(String[] args) {
		
		// 1. 배열 메모리 생성
		// 5일 동안의 온도를 정수로 저장하기 위해 'temp'라는 이름의 배열을 만듭니다.
		int[] temp = new int[5];	
		
		// 2. 각 칸에 접근해서 값 저장하기
		// 월, 화, 수, 목, 금요일의 온도를 차례대로 저장합니다.
		temp[0] = 25; 
		temp[1] = 27; 
		
		// ⚠️ 주의: 수요일(3번째 날)의 온도를 저장하려고 합니다. 알맞은 인덱스 번호는 무엇일까요?
		temp[2] = 26; 	
		temp[3] = 29; 
		temp[4] = 28; 
		
		//[25, 27, 26, 29, 28]  temp 배열 
		// 0   1   2   3   4  index
		
		System.out.println("=== 이번 주 최고 온도 ===");
		
		// 3. for 반복문을 이용하여 데이터 차례대로 꺼내기
		// 배열의 첫 번째 칸부터 마지막 칸까지 꺼내기 위해 시작값과 조건식을 채워주세요.
		for(int i = 0; i < 5; i++) {
			
			System.out.println("temp[" + i + "] = " + temp[i] + "도");
			
		}

	}
}




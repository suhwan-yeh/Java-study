// 더 자세한 텍스트 기반(ASCII) 메모리 모델링 주석 버전
// 비전공자도 메모리에 어떻게 값이 쌓이는지 보이도록 설명 강화

public class Arr04_1 {

    public static void main(String[] args) {
        /*
         * ===============================
         * ▶ 2차원 배열 메모리 구조 (ASCII 모델링)
         * ===============================
         *  scores 배열은 "행 = 학생", "열 = 과목" 으로 구성된 표 형태의 구조다.
         *
         *  배열 선언 시 아래와 같이 값이 저장된다:
         *
         *       열 번호 →      [0]     [1]     [2]
         *                 ----------------------------
         *  행[0] (1번 학생) |   85      60      70
         *  행[1] (2번 학생) |   90      95      80
         *  행[2] (3번 학생) |   75      80     100
         *  행[3] (4번 학생) |   80      70      95
         *  행[4] (5번 학생) |  100      65      80
         *                 ----------------------------
         *
         *  즉, scores[행][열] 로 접근한다.
         *  예) scores[2][1] → 80 (3번 학생의 영어)
         *
         * ------------------------------
         * 행 = 학생(세로) / 열 = 과목(가로)
         * ------------------------------
         */

        int[][] scores = {
                {85, 60, 70},   // 0행
                {90, 95, 80},   // 1행
                {75, 80,100},   // 2행
                {80, 70, 95},   // 3행
                {100,65, 80}    // 4행
        };
        				  //2차원배열명.length;
        				  //의미 : 2차원배열의 행 갯수 얻는다
        
        int studentCount = scores.length;       // 5명
        
        				 //2차원배열명[행index].length;
        				 //의미 : 행index의 행에 만들어진 열갯수 얻는다
       
        int subjectCount = scores[0].length;    // 3과목

        /*
         * =======================================
         * ▶ 학생별 총점 배열 구조 (ASCII 모델링)
         * =======================================
         * studentTotal = new int[5];
         * 메모리 모습:
         *
         *   인덱스 →    [0]   [1]   [2]   [3]   [4]
         *             --------------------------------
         *   값     →    0     0     0     0     0
         *             --------------------------------
         *   의미   →     1번   2번   3번    4번    5번 학생 총점
         */
        int[] studentTotal = new int[studentCount];

        /*
         * =======================================
         * ▶ 과목별 총점 배열 구조 (ASCII 모델링)
         * =======================================
         * subjectTotal = new int[3];
         *
         *   인덱스 →  [0]   [1]   [2]
         *           -------------------
         *   값     →   0     0     0
         *           -------------------
         *   의미   → 국어  영어  수학 총점
         */
        int[] subjectTotal = new int[subjectCount];


        System.out.println("[학생별 총점 계산]");

        /*
         * ==============================================
         * ▶ 학생별 총점 계산 과정 (텍스트 흐름 모델링)
         * ==============================================
         *
         * 예시: 1번 학생(행 0)의 점수 합산 과정
         *
         *   studentTotal[0] = 0
         *   - scores[0][0] = 85 → studentTotal[0] = 85
         *   - scores[0][1] = 60 → studentTotal[0] = 85 + 60 = 145
         *   - scores[0][2] = 70 → studentTotal[0] = 145 + 70 = 215
         *
         *   최종 → 215 저장
         *
         * 같은 방식으로 모든 행(학생) 반복
         */

        for (int r = 0; r < studentCount; r++) {
            // r = 학생
            for (int c = 0; c < subjectCount; c++) {
                // c = 과목
                studentTotal[r] += scores[r][c];
            }
            System.out.printf("%d번 학생 총점: %d점%n", r + 1, studentTotal[r]);
        }


        System.out.println();
        System.out.println("[과목별 총점 계산]");

        /*
         * ======================================================
         * ▶ 과목별 총점 계산 과정 (텍스트 기반 메모리 흐름 모델링)
         * ======================================================
         *
         * 예) 국어(열 0)의 총점 계산:
         *
         *   subjectTotal[0] = 0
         *   - scores[0][0] = 85 → 85
         *   - scores[1][0] = 90 → 175
         *   - scores[2][0] = 75 → 250
         *   - scores[3][0] = 80 → 330
         *   - scores[4][0] = 100 → 430
         *
         *   최종 국어 총점 = 430
         *
         * 영어(열 1), 수학(열 2)도 동일 방식
         */

        for (int c = 0; c < subjectCount; c++) {
            for (int r = 0; r < studentCount; r++) {
                subjectTotal[c] += scores[r][c];
            }
            System.out.printf("%d번 과목 총점: %d점%n", c + 1, subjectTotal[c]);
        }
    }
}
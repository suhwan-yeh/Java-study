// ★ 문제1. 포켓몬(Pokemon) 배틀 클래스 만들기 ★
//--------------------------------------------------------------------------
// 스타크래프트 Unit 예제와 똑같은 구조로 포켓몬 클래스를 완성하세요.
// 아래 주석의 지시대로 코드를 직접 작성하면 됩니다.
//==========================================================================

public class Pokemon { //포켓몬 설계도

	//1. 객체 변수 선언 - 포켓몬의 데이터 정보 저장할 용도
	//   String name;   -> 포켓몬 이름 (예: "피카츄", "꼬부기", "리자몽") 저장
	//   int    hp;     -> 포켓몬 기본체력 저장
	//   int    power;  -> 포켓몬 기술공격력 저장
	
	//>>> 여기에 객체 변수 3개를 선언하세요.
	String name;
	int hp;
	int power;
		
	//----------------------------------------------------------------
	//[풀이] 생성자 3개를 왜 이렇게 나눴나?
	//  - "이름만 아는 경우" / "이름+체력만 아는 경우" / "다 아는 경우"
	//    이렇게 3가지 상황이 있을 수 있으므로 생성자를 3개 만든다(오버로딩).
	//  - 매번 초기화 코드를 반복해서 쓰면 실수하기 쉽고 코드가 길어진다.
	//    그래서 "제일 상세한 생성자"(3번째) 하나만 실제 초기화를 담당하고,
	//    나머지 생성자는 this(...)로 그 생성자를 "대신 호출"만 해준다.
	//  - this(...)는 반드시 생성자의 "첫 줄"에만 쓸 수 있다는 규칙 주의!
	//----------------------------------------------------------------
	//2. 첫번째 생성자 : 포켓몬 이름만 매개변수로 받는 생성자
	//   (체력 : 100, 공격력 : 15 로 고정)
	//   ★ this() 생성자 호출구문을 사용해서 세번째 생성자에게 넘기세요.
	
	//>>> 여기에 첫번째 생성자를 작성하세요.
	//   [풀이] 체력/공격력을 정하지 않았으므로 기본값(100, 15)을
	//         세번째 생성자에게 대신 넘겨서 처리를 위임한다.
	public Pokemon(String name) {
		this(name, 100,  15);
	}
	
	//   두번째 생성자 : 포켓몬 이름과 체력을 매개변수로 받는 생성자
	//   (공격력 : 15 로 고정)
	//   ★ this() 생성자 호출구문을 사용하세요.
	
	//>>> 여기에 두번째 생성자를 작성하세요.
	//   [풀이] 공격력만 빠졌으므로 공격력 기본값(15)만 채워서
	//         역시 세번째 생성자에게 넘긴다.
	public Pokemon(String name, int hp) {
		this(name, hp, 15);
	}
	
	//   세번째 생성자 : 이름, 체력, 공격력 모두 매개변수로 받는 생성자
	//   ★ this 키워드를 사용하여 모든 객체변수값을 초기화하세요.
	//   (힌트: this.name = name; 형식)
	
	//>>> 여기에 세번째 생성자를 작성하세요.
	//   [풀이] ★진짜 초기화가 일어나는 유일한 생성자★
	//          this.name / this.hp / this.power 는 "이 객체의" 필드를 의미하고,
	//          오른쪽 name/hp/power는 매개변수를 의미한다.
	//          이름이 같아서 헷갈릴 수 있는데, this.붙은 쪽이 "객체 소유",
	//          안 붙은 쪽이 "잠깐 전달받은 값"이라고 구분하면 된다.
	public Pokemon(String name, int hp, int power) {
						//"리자몽", 180,     40
		this.name = name;
		this.hp = hp;
		this.power = power;
	}	
	
	
	//3. 포켓몬의 행동을 메소드로 표현하기 위해 메소드 선언
	
	//첫번째 메소드
	//메소드명 : skill
	//매개변수 : Pokemon target  (공격 당할 상대 포켓몬 객체)
	//기능 순서:
	//  조건문1 : 현재 포켓몬(this)의 hp가 0보다 작거나 같으면
	//            "OOO은(는) 기절해서 기술을 쓸 수 없습니다!" 출력 후 return
	//  조건문2 : target 포켓몬의 hp가 0보다 작거나 같으면
	//            "OOO은(는) 이미 기절했습니다!" 출력 후 return
	//  공격처리 : target의 hp를 this의 power만큼 차감
	//            (힌트: target.hp -= this.power;)
	//  출력    : "OOO이(가) OOO을(를) 공격합니다!(공격력:XX)"
	//  마무리  : target의 hp가 0이하가 되면 hp를 0으로 고정하고
	//            "OOO이(가) 기절했습니다!!" 출력
	
	//>>> 여기에 skill 메소드를 작성하세요.
	public void skill(Pokemon target) {
		
		//[풀이-조건문1] 죽은(기절한) 애가 공격하는 건 말이 안 되므로
		//               제일 먼저 자기 자신 상태부터 체크한다.
		//               return을 만나면 메소드 실행이 즉시 끝난다(중괄호까지 안 감).
		if(this.hp <= 0) {
			 System.out.println(this.name + "은(는) 기절해서 기술을 쓸 수 없습니다!");
			 return;
		}
		//[풀이-조건문2] 이미 기절한 상대를 또 때릴 필요는 없으므로
		//             상대 상태도 미리 체크해서 불필요한 처리를 막는다.
		if(target.hp <= 0) {
			System.out.println(target.name + "은(는) 이미 기절했습니다!"); 
			return;
		}
		
		//[풀이] 여기까지 내려왔다는 건 "둘 다 살아있다"는 뜻.
		//  공격처리 : target의 hp를 this의 power만큼 차감
		//            (힌트: target.hp -= this.power;)
		target.hp -= this.power;
		
		//출력    : "OOO이(가) OOO을(를) 공격합니다!(공격력:XX)"
		System.out.println(this.name + "이(가)" + target.name + "을(를) 공격합니다!(공격력:" + this.power +")");
		
		
		//[풀이] 데미지를 준 "직후"에 다시 한번 체력을 체크하는 이유:
		//       빼기 연산 특성상 target.hp가 음수(-5 같은)가 될 수 있는데,
		//       음수 체력이 화면에 찍히면 어색하므로 0으로 보정해준다.
		
		//  마무리  : target의 hp가 0이하가 되면 hp를 0으로 고정하고
		//            "OOO이(가) 기절했습니다!!" 출력
		if(target.hp <= 0) {
			target.hp = 0;
			System.out.println(target.name + "이(가) 기절했습니다!!");
		}
		
	} //----------------skill
	
	
	//두번째 메소드
	//메소드명 : status
	//기능 : 현재 포켓몬 상태 출력
	//출력형식 : [포켓몬 상태] 이름 - 체력: XX, 공격력: XX
	
	//[풀이] 단순히 필드 3개를 그대로 출력만 하는 메소드.
	//       "지금 이 객체가 어떤 상태인지" 확인용으로
	//       전투 전/후 비교할 때 자주 호출하게 된다.
	//>>> 여기에 status 메소드를 작성하세요.
	public void status() {
		System.out.println("[포켓몬 상태]"+ this.name +" - 체력: " + this.hp + ", 공격력: " + this.power);
	}
	
	public static void main(String[] args) {
		
		//Pokemon클래스의 첫번째 객체 생성 : 이름만 받는 생성자 호출
		//  변수명 pikachu, 이름 "피카츄"  -> 체력 100, 공격력 15 자동 설정
			
			//>>> 여기에 피카츄 객체 생성 코드를 작성하세요.
			Pokemon pikachu = new Pokemon("피카츄");
		
		//Pokemon클래스의 두번째 객체 생성 : 이름과 체력을 받는 생성자 호출
		//  변수명 squirtle, 이름 "꼬부기", 체력 90
		
			//>>> 여기에 꼬부기 객체 생성 코드를 작성하세요.
			Pokemon squirtle = new Pokemon("꼬부기", 90);
			
		
		//Pokemon클래스의 세번째 객체 생성 : 이름, 체력, 공격력 모두 받는 생성자 호출
		//  변수명 charizard, 이름 "리자몽", 체력 180, 공격력 40
		
			//>>> 여기에 리자몽 객체 생성 코드를 작성하세요.
			Pokemon charizard = new Pokemon("리자몽", 180, 40);
		
		
		//위 생성된 3개의 Pokemon객체 상태 출력 (status 메소드 호출)
		
			//>>> 여기에 status 호출 3줄을 작성하세요.
			pikachu.status();     squirtle.status();    charizard.status();
		
		
		
		System.out.println("\n==== 배틀 시작 ====");
		
		//>>> 여기에 배틀 코드를 작성하세요.
		
		//배틀 시뮬레이션 - 아래 순서대로 skill 메소드를 호출하세요.
		//  ① 피카츄가 꼬부기 공격
		pikachu.skill(squirtle);
		
		//  ② 꼬부기가 피카츄 공격
		squirtle.skill(pikachu);
		
		//③ 리자몽이 꼬부기 공격 x 2번 (90 -> 50 -> 10... 실제로는 40씩 두번 = 80, 90-80=10? 확인 필요)
		charizard.skill(squirtle);  charizard.skill(squirtle);
					
		//  ④ 리자몽이 피카츄 공격 x 3번
		charizard.skill(pikachu);  charizard.skill(pikachu);  charizard.skill(pikachu);

		
		System.out.println("\n==== 배틀 종료 후 상태 ====");
		
		//>>> 여기에 status 호출 3줄을 작성하세요.
		pikachu.status();
		squirtle.status();
		charizard.status();	
	}

}


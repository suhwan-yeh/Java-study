/*
======================================================================
  [핵심 개념] 논리 연산자 (AND, OR, NOT)
======================================================================
  조건이 여러 개일 때, 이것들을 조합해서 최종적으로 참/거짓을 판별합니다.

  [1] && (AND, 그리고)
  - 양쪽 조건이 **모두 맞아야만** true를 반환합니다. (깐깐한 성격)
  - 예: "키가 180 이상이고(AND) 몸무게가 70 이상인가요?"

  [2] || (OR, 또는)
  - 양쪽 조건 중 **하나라도 맞으면** true를 반환합니다. (너그러운 성격)
  - 예: "학생이거나(OR) 군인인가요?"

  [3] ! (NOT, 반대)
  - 결과를 청개구리처럼 **반대로** 뒤집습니다.
  - 예: true -> false / false -> true
*/

public class Opr04 {

	public static void main(String[] args) {
		
		int age=15;
		
        /*
         * [질문] 이 사람은 10대 입니까?
         * 10대의 조건: 10살 이상 '이고(&&)' 19살 이하.
         */
		boolean result = age >= 10 && age <= 19;
		//				  15 >= 10 && 15  <= 19;
		//					true   &&    true;
		//						  true;
		
		System.out.println("age변수 나이 결과는 " + result);
		
		
	}

}











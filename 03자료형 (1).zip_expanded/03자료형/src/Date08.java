

//예제 : 문자열(String) 저장하기

public class Date08 {

	public static void main(String[] args) {
	    /*
	     * --------------------------------------------------
	     * [1] 문자 (char) vs 문자열 (String) 구분하기
	     * --------------------------------------------------
	     * 1. 문자 (char)
	     * - 오직 "단 1개의 글자"만 저장할 수 있습니다.
	     * - 반드시 작은 따옴표('')로 감싸야 합니다.
	     * 예) 'A', '가', '3'
	     * - // char a = 'AB';  <- [컴파일 에러] 두 글자 이상은 저장 불가!
	     * 
	     * * 2. 문자열 (String)
	     * - "두 글자 이상"의 단어나 문장을 한꺼번에 저장할 때 사용합니다.
	     * - 반드시 큰 따옴표("")로 감싸야 합니다.
	     * 예) "안녕", "1234", "AB"
	     */

		
	    /*
	     * --------------------------------------------------
	     * [2] 문자열 상수를 변수에 저장하기
	     * --------------------------------------------------
	     * 문자열 데이터를 담기 위해서는 일반 기본 자료형(int, char 등)이 아니라,
	     * 자바에서 특별히 제공하는 'String 클래스' 자료형을 사용해야 합니다.
	     * * [문법] String 변수이름 = "저장할 문자열";
	     */
		//1. 문자열을 담을 수 있는 'y'라는 이름의 변수 선언
		String y;
		
		//2. 변수 y에 문자열 "AB"를 대입 (큰 따옴표 사용 필수!)
		y = "AB";
		
		System.out.println("y 변수에 저장된 문자열 : " + y);
	    /*
	     * --------------------------------------------------
	     * [주의사항] 
	     * 글자가 단 1개뿐이더라도, 큰 따옴표("")로 감싸면 
	     * 컴퓨터는 무조건 '문자열(String)'로 취급합니다.
	     * 예) 'A' (문자)  vs  "A" (문자열) -> 이 둘은 완전히 다른 데이터입니다!
	     * --------------------------------------------------
	     */		
		
		
	}

}






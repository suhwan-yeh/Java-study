/*
======================================================================
  [핵심 개념] 문자(char) 데이터의 비밀과 대소 비교
======================================================================
  자바에서 문자는 겉보기엔 글자('A', 'b')지만, 컴퓨터 속에는 '숫자(정수)'로 저장됩니다.
  (예: 대문자 'A'는 65, 소문자 'a'는 97)
  
  따라서 문자끼리도 누가 더 큰지 관계 연산자(>, <)로 비교가 가능합니다!
  
  * 알파벳 대문자는 65('A') 부터 90('Z') 까지 차례대로 배정되어 있습니다.
*/
public class Opr06 {
	public static void main(String[] args) {
		
		char ch = 'b'; //변수 ch에 소문자 'b' (숫자로는 98) 저장
		String s;      //결과를 담을 문자열 변수 
		      
        /*
         * [질문] ch에 담긴 글자가 대문자 범위('A' ~ 'Z') 안에 속하나요?
         * 즉, 'A'(65) 보다는 크거나 같고(AND) 'Z'(90) 보다는 작거나 같은가요?
         */
		boolean result = ch >= 'A' &&  ch <= 'Z';
		//				 'b'>= 'A' &&  'b'<= 'Z';
		//                98>= 65  &&   98<=  90;
		//                  true   &&      false;
		//							false;
		
		//앞에서 배운 삼항 연산자를 이용해 true이면 "대문자", false이면 "대문자 아님" 대입
		s = result ? "대문자입니다." : "대문자가 아닙니다.";
		   
		System.out.println(ch + " => " + s);
		//'b'는 소문자이므로 범위를 벗어나 "대문자가 아닙니다."  출력 

	}

}







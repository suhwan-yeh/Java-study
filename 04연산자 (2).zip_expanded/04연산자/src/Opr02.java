/*
======================================================================
  [핵심 개념] 관계 연산자 (비교 연산자)
======================================================================
  두 데이터의 크기를 비교하여 "맞다(true)" 혹은 "틀리다(false)"로 
  대답해주는 연산자입니다.

  [관계 연산자 종류]
  >   (크다)       : 6 > 2  (맞음 -> true)
  >=  (크거나 같다)  : 2 >= 5 (틀림 -> false)
  <   (작다)       : 2 < 3  (맞음 -> true)
  <=  (작거나 같다)  : 8 <= 3 (틀림 -> false)
  ==  (같다)       : 8 == 3 (틀림 -> false)  ※ 자바에서 '같다'는 기호 2개(==)를 씁니다!
  !=  (다르다)     : 8 != 6 (맞음 -> true)
*/
public class Opr02 {

	public static void main(String[] args) {
		
		int a=10, b=4;

		boolean test;  // 비교 연산의 결과 값인 true 또는 false가 향후에 저장될 변수 선언
		
		//a(10) 이 b(4)보다 더 크냐? 라고 묻는 비교연산식 작성하여 컴퓨터에게 물어보면? 컴퓨터는 a > b식으로 해석해서 true
		test = a >  b;
		System.out.println(a + " > " + b + " = " + test);
		
		//a(10)가 b(4)보다 더 작니? 라고 묻습니다. 틀렸으니 false
		test = a  < b;
		System.out.println(a + " < " + b + " = " + test);
		
		//a(10) + b(4)결괏값이  a(10) - b(4)결괏값보다 더 크니? 맞으니 true
		test = (a + b > a - b);
		// 풀이: (14) > (6)  --> true
		System.out.println(a + "+" + b + ">" + a + "-" + b + "=" + (a+b>a-b) );
		
	}


}











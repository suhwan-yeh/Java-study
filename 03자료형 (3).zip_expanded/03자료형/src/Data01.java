
//예제 : 자바에서 사용되는 상수의 종류 

public class Data01 {
	
	//main 메소드 
	//기능 : 자바프로그램이 처음 시작되게 하는 기능 
	public static void main(String[] args) {
		
		//(1) 정수 : 소수점이 없는 수 
		System.out.println(1);
		
		//(2) 실수 : 소수점이 있는 수 
		System.out.println(1.5);
		
		//(3) 문자 : 단일 따옴표로 묶은 데이터
		System.out.println('a');
		
		//(4) 논리값 : true , false 데이터
		System.out.println(true);
		
		
	}

}


//예제 : 문자 저장하기 

public class Date07 {

	public static void main(String[] args) {
	    /*
	     * --------------------------------------------------
	     * [핵심 개념] 문자와 유니코드 (아스키코드)
	     * --------------------------------------------------
	     * 컴퓨터는 'A'나 'a' 같은 문자를 있는 그대로 저장하지 못하고,
	     * 오직 숫자(0과 1)로만 기억할 수 있습니다.
	     * 따라서 각 문자에 고유한 [정수 번호]를 매겨 두었는데 이를 '유니코드'라고 합니다.
	     * * [암기하면 유용한 대표 유니코드 (10진수)]
	     * 대문자 'A' -> 정수 65
	     * 소문자 'a' -> 정수 97
	     * 숫자모양 문자 '0' -> 정수 48
	     */
		
		 //2바이트 크기의 단일 문자를 저장할 수 있는 char형 변수 선언
		char x;
		
	    // 변수 x에 문자 'A'를 저장! 
	    // (작은 따옴표 '' 사용에 주의하세요. 실제 메모리에는 65라는 10진수의 이진수가 저장됩니다.)
		x = 'A';
		
	    /*
	     * --------------------------------------------------
	     * [실습] 문자를 숫자로 변환해서 확인하기
	     * --------------------------------------------------
	     * 변수 x에 들어있는 값('A')이 실제로 컴퓨터 내부에서 어떤 정수 번호인지 확인하려면,
	     * '(int)x' 처럼 정수형으로 [강제 형변환(캐스팅)]을 하면 숫자가 튀어나옵니다.
	     */
		
		 // 출력 형식 지정자: %c (문자), %d (10진수 정수), \n (줄바꿈)
		System.out.printf("문자 %c의 유니코드 정수 번호는 -> %d 입니다.\n", x, (int)x);
		//                  문자 A의 유니코드 정수 번호는 -> 65 입니다.
		
	    // 이번에는 숫자 모양의 '문자'인 '0'을 넣어봅시다. (실제 정수 0이 아님!)
		x = '0';
		System.out.printf("문자 %c의 유니코드 정수 번호는 -> %d 입니다.\n",  x, (int)x);
	    //					문자 0의 유니코드 정수 번호는 -> 48 입니다.  
	}

}



















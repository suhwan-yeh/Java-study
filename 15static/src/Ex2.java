


// 주제 : 자바에서  클래스 안의  멤버 메소드 살펴보기 


class  AAA {
	
	//멤버변수 종류 :  객체변수, 클래스변수 
			int  num;    	//객체 변수 :  객체를 생성할떄마다 따로 존재하며, JVM의 heap영역에 올라갑니다.
	static  int staticNum;  //클래스 변수 :  class AAA가 JVM의 Method area영역에 올라갈때 함께 생성되어 올라가는 변수
	
	//생성자 종류  :  기본생성자, 매개변수가 작성된 생성자
	public AAA() {}   //기본생성자 
	
	//멤버메소드 종류 :  객체메소드, 클래스메소드 
	
	//객체메소드1
	//- 객체가 생성된 후에만 호출가능한 메소드
	//- static 예약어가 작성되어 있지 않는 메소드
	//- 객체 메소드 내부에서  객체변수와  클래스변수 모두 접근(저장된 값 불러오거나 저장) 가능 
	//- 객체 메소드 내부에서는  this 키워드를 통해  객체 메모리 자신의 객체변수에 접근(저장된 값 불러오거나 저장) 가능
	public /*static*/ void add(int value) {
			  this.num += value;  //<- 객체 변수 num에 누적
		AAA.staticNum  += value;  //<- 클래스 변수 staticNum에 누적
	}
	
	//객체메소드2
	//기능 : 객체변수 num의 값을 외부로 반환
	public  int getNum() {
		
		return this.num;      //<-  객체 변수 num의 값을 외부로 반환 
	}
	
	//객체메소드3
	//기능 : 객체변수 num의 값을 변경 하고, 클래스변수 staticNum의 값을 변경 
	public  void setNum(int num) {
		
		this.num = num; 		 //<- 객체 변수 num의 값을 매개변수 num의 값으로 변경
		AAA.staticNum = num;	 //<- 클래스변수 staticNum의 값을 매개변수 num의 값으로 변경 	
	}
	
	//클래스 메소드1
	//- 클래스명으로 호출 가능하고, 객체 메모리 생성 없이도 호출 가능한 메소드.
	//- static 작성해서 만든 메소드.
	//- 클래스 메소드 내부에서는  클래스변수에만 접근 가능!  객체변수는 접근 불가능!
	//- 클래스 메소드 내부에서는  this 예약어 작성 불가능!
	public static  int getStaticNum() {
		
		  return AAA.staticNum;      //<- 클래스변수 staticNum의 값을 외부로 반환 가능 
		//return   this.num;         //<- 객체 변수 num의 값을 외부로 반환 불가능
	}
	
	//클래스 메소드2
	//기능 : 매개변수로 전달 받은 값을 클래스변수에 변경 
	public static void setStaticNum(int staticNum) {
		
		AAA.staticNum = staticNum;   //<- 클래스변수 staticNum의 값 변경 가능
	  //this.num = staticNum;        //<- 객체변수 num의 값 변경 불가능
		
	}
	
}


public class Ex2 {

	//클래스 메소드 입니다.
	//기능 : 자바 프로그램을 처음 가동 시키는 기능 
	public static void main(String[] args) {
		// 【0단계: 클래스 로딩 + main 시작 직후】 (세로 그림)
		//
		// ┌─────────────────────────────────────┐
		// │ Method Area                         │
		// │ ┌─────────────────────────────────┐ │
		// │ │ class AAA                       │ │
		// │ │   static int staticNum = 0      │ │ ← 클래스 로딩 시 생성
		// │ │   getStaticNum(),setStaticNum() │ │
		// │ │ class Ex2                       │ │
		// │ │   main()                        │ │
		// │ └─────────────────────────────────┘ │
		// ├─────────────────────────────────────┤
		// │ Stack                               │
		// │ ┌─────────────────────────────────┐ │
		// │ │ main() 프레임                     │ │
		// │ │   aaa [ null ]                  │ │ ← 아직 객체 생성 전
		// │ └─────────────────────────────────┘ │
		// ├─────────────────────────────────────┤
		// │ Heap                                │
		// │        (비어있음)                      │
		// └─────────────────────────────────────┘
		
		//1.   class AAA 하나를 이용해  객체 생성 : JVM의 Heap 영역에  new AAA();로 생성한 객체 메모리가 생성되어 올라감
		AAA   aaa  = new AAA();              //aaa 참조변수는 JVM의  Static영역에 만들어지고, Heap영역의 객체 메모리 참조함
		
		// 【1단계: new AAA() 실행 후】 (세로 그림)
		//
		// ┌─────────────────────────────────────┐
		// │ Method Area                         │
		// │ ┌─────────────────────────────────┐ │
		// │ │ class AAA                       │ │
		// │ │   static int staticNum = 0      │ │ ← 아직 0
		// │ └─────────────────────────────────┘ │
		// ├─────────────────────────────────────┤
		// │ Stack                               │
		// │ ┌─────────────────────────────────┐ │
		// │ │ main() 프레임                     │ │
		// │ │   aaa [ 0x100 ] ──┐             │ │ ← 참조변수는 객체 "주소"만 저장
		// │ └───────────────────┼─────────────┘ │
		// ├─────────────────────┼───────────────┤
		// │ Heap                │               │
		// │ ┌───────────────────▼─────────────┐ │
		// │ │ AAA 객체 @0x100                  │ │ ← aaa가 참조하는 객체
		// │ │   int num = 0                   │ │ ← 인스턴스변수는 객체 안에 생성
		// │ └─────────────────────────────────┘ │
		// └─────────────────────────────────────┘
		
		//[Method Area] 영역에 올라간 staticNum 클래스변수값 변경
		AAA.staticNum  =  50;   //방법1. 클래스명.클래스변수명 = 변경할값;
		aaa.staticNum  =  50;  	//방법2.  참조변수명.클래스변수명 = 변경할값;
		
		// 【2단계: staticNum = 50 저장 후】 (세로 그림)
		//  ★두 줄(AAA.staticNum / aaa.staticNum) 모두 "같은" Method Area의
		//    staticNum 하나를 바꾼 것! Heap의 num은 전혀 변하지 않음★
		//
		// ┌─────────────────────────────────────┐
		// │ Method Area                         │
		// │ ┌─────────────────────────────────┐ │
		// │ │ class AAA                       │ │
		// │ │   static int staticNum = 50     │ │ ← 0 -> 50 으로 변경됨
		// │ └─────────────────────────────────┘ │
		// ├─────────────────────────────────────┤
		// │ Stack                               │
		// │ ┌─────────────────────────────────┐ │
		// │ │ main() 프레임                     │ │
		// │ │   aaa [ 0x100 ]                 │ │
		// │ └─────────────────────────────────┘ │
		// ├─────────────────────────────────────┤
		// │ Heap                                │
		// │ ┌─────────────────────────────────┐ │
		// │ │ AAA 객체 @0x100                  │ │
		// │ │   int num = 0                   │ │ ← 그대로 0 (안 건드림)
		// │ └─────────────────────────────────┘ │
		// └─────────────────────────────────────┘
		
		
		// static 이 작성된 class AAA 에 포함되어  JVM의 Method Area에 올라가있는 클래스 메소드들 호출해보자
		int result = AAA.getStaticNum();     //방법1.  클래스명.클래스메소드명();
		System.out.println(result);  //50
		
		    result = aaa.getStaticNum();   //방법2.  참조변수명.클래스메소드명();
		System.out.println(result);  //50
		
		// 【3단계: getStaticNum() 호출 "순간"】 (세로 그림)
		//  ★메소드를 호출하면 Stack에 프레임이 "위로" 잠깐 쌓였다가,
		//    return 되면 사라진다★
		//
		// ┌─────────────────────────────────────┐
		// │ Method Area                         │
		// │ ┌─────────────────────────────────┐ │
		// │ │ class AAA                       │ │
		// │ │   static int staticNum = 50    │ │ ←─┐ 여기 값을 읽어서 반환
		// │ │   getStaticNum() 코드           │ │    │
		// │ └─────────────────────────────────┘ │   │
		// ├─────────────────────────────────────┤   │
		// │ Stack                               │   │
		// │ ┌─────────────────────────────────┐ │   │
		// │ │ getStaticNum() 프레임           │ │ ──┘ ← 호출 순간 새로 쌓임
		// │ │   (this 없음! - static이므로)      │ │      (return 후 즉시 소멸)
		// │ ├─────────────────────────────────┤ │
		// │ │ main() 프레임                    │ │
		// │ │   aaa [ 0x100 ]                 │ │
		// │ └─────────────────────────────────┘ │
		// ├─────────────────────────────────────┤
		// │ Heap                                │
		// │ ┌─────────────────────────────────┐ │
		// │ │ AAA 객체 @0x100                 │ │
		// │ │   int num = 0                   │ │ ← static 메소드는 여기 접근 불가!
		// │ └─────────────────────────────────┘ │     (this가 없으니 어느 객체의
		// └─────────────────────────────────────┘      num인지 알 방법이 없음)
		
/*
        📌 결론 정리
        1️ 객체 변수 vs 클래스 변수
           - 객체 변수(num): 객체마다 개별로 존재 (heap 영역)
           - 클래스 변수(staticNum): 클래스당 하나만 존재 (Method Area)

        2️ 객체 메소드 vs 클래스 메소드
           - 객체 메소드:
             ▸ static 키워드가 없음
             ▸ 객체가 생성되어야 호출 가능
             ▸ this 사용 가능 (자기 자신의 객체 변수에 접근)
             ▸ 객체 변수와 클래스 변수 모두 접근 가능

           - 클래스 메소드(static):
             ▸ static 키워드 있음
             ▸ 객체 생성 없이 클래스명으로 호출 가능
             ▸ this 사용 불가능
             ▸ 클래스 변수(static 변수)만 접근 가능, 객체 변수에는 접근 불가

        3️ 메모리 구조 시각화 요약 (세로 방향 - 최종 상태)

        ┌─────────────────────────────┐
        │ Method Area                 │
        │   class AAA                 │ ← 클래스 로드 시 static 요소 저장
        │   static staticNum = 50     │
        ├─────────────────────────────┤
        │ Stack                       │
        │   main() 프레임               │ ← 프로그램 종료 시 소멸
        │     aaa [ 0x100 ]           │
        ├─────────────────────────────┤
        │ Heap                        │
        │   AAA 객체 @0x100             │ ← 객체 생성 시 인스턴스 변수 저장
        │     int num = 0             │
        └─────────────────────────────┘
		*/		
	}

}








